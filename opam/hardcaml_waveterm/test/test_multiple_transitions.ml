module%test _ = struct
  module Make (C : sig
      val cycles_per_char : int
    end) =
  struct
    module I = struct
      type 'a t =
        { clk : 'a
        ; i : 'a
        }
      [@@deriving hardcaml]
    end

    module D = struct
      type 'a t =
        { o : 'a
        ; p : 'a [@bits 2]
        }
      [@@deriving hardcaml]
    end

    module O = struct
      type 'a t = { o : 'a D.t list [@length C.cycles_per_char + 1] }
      [@@deriving hardcaml]
    end

    let create (i : _ I.t) =
      let open Signal in
      let spec = Reg_spec.create ~clock:i.clk () in
      let rec f i x =
        if i = 0
        then []
        else (
          let y = reg spec x in
          x :: f (i - 1) y)
      in
      let o = f (C.cycles_per_char + 1) i.i in
      let p = f (C.cycles_per_char + 1) (i.i @: i.i) in
      assert (List.length o = C.cycles_per_char + 1);
      { O.o = List.map2_exn o p ~f:(fun o p -> { D.o; p }) }
    ;;

    let test ?override_wave_width () =
      let open Bits in
      let module Sim = Cyclesim.With_interface (I) (O) in
      let sim = Sim.create create in
      let waves, sim = Cyclesim.Waveform.create sim in
      let i = Cyclesim.inputs sim in
      for n = 1 to C.cycles_per_char + 2 do
        i.i := gnd;
        Cyclesim.cycle ~n:(C.cycles_per_char * 2) sim;
        i.i := vdd;
        Cyclesim.cycle ~n sim;
        i.i := gnd;
        Cyclesim.cycle ~n:((C.cycles_per_char - n) % C.cycles_per_char) sim;
        Cyclesim.cycle ~n:(C.cycles_per_char * 2) sim
      done;
      Cyclesim.cycle ~n:200 sim;
      Waveform.expect_exact
        waves
        ~wave_width:(Option.value override_wave_width ~default:(-C.cycles_per_char))
        ~display_rules:
          Display_rule.(
            List.concat
              [ [ port_name_is "clk" ]
              ; (let module R = Display_rules.With_interface (O) in
                R.default ())
              ])
    ;;
  end

  let test ?override_wave_width cycles_per_char =
    let module T =
      Make (struct
        let cycles_per_char = cycles_per_char
      end)
    in
    T.test ?override_wave_width ()
  ;;

