open! Import

module Binary = struct
  module I = struct
    type 'a t =
      { clk : 'a
      ; a : 'a [@bits 1]
      }
    [@@deriving hardcaml]
  end

  module O = struct
    type 'a t =
      { b : 'a [@bits 1]
      ; c : 'a [@bits 1]
      ; d : 'a [@bits 1]
      ; e : 'a [@bits 1]
      ; f : 'a [@bits 1]
      }
    [@@deriving hardcaml]
  end

  let create (i : Signal.t I.t) =
    let open Signal in
    let reg_spec = Reg_spec.create () ~clock:i.clk in
    let b = reg reg_spec i.a in
    let c = reg reg_spec b in
    let d = reg reg_spec c in
    let e = reg reg_spec d in
    let f = reg reg_spec e in
    { O.b; c; d; e; f }
  ;;
end

module Data = struct
  module I = struct
    type 'a t =
      { clk : 'a
      ; a : 'a [@bits 1]
      }
    [@@deriving hardcaml]
  end

  module O = struct
    type 'a t =
      { b : 'a [@bits 4]
      ; c : 'a [@bits 4]
      ; d : 'a [@bits 4]
      ; e : 'a [@bits 4]
      ; f : 'a [@bits 4]
      }
    [@@deriving hardcaml]
  end

  let create (i : Signal.t I.t) =
    let open Signal in
    let reg_spec = Reg_spec.create () ~clock:i.clk in
    let a_prev = reg reg_spec i.a in
    let a_rise = i.a &: ~:a_prev in
    let b = reg_fb reg_spec ~enable:a_rise ~width:4 ~f:(fun b -> b +:. 1) in
    let c = reg reg_spec b in
    let d = reg reg_spec c in
    let e = reg reg_spec d in
    let f = reg reg_spec e in
    { O.b; c; d; e; f }
  ;;
end

let init_binary_test () =
  let open Binary in
  let module Sim = Cyclesim.With_interface (I) (O) in
  let sim = Sim.create ~config:Cyclesim.Config.trace_all create in
  Cyclesim.Waveform.create sim
;;

let init_data_test () =
  let open Data in
  let module Sim = Cyclesim.With_interface (I) (O) in
  let sim = Sim.create ~config:Cyclesim.Config.trace_all create in
  Cyclesim.Waveform.create sim
;;

